<li>
The daily and monthly verification processing is broken down into three main programs: QCMON, SUMAC4 and ANLVER. The QCMON monthly processing produces reports of aircraft; AMDAT/ASDAR; ACARS; satellite wind; ship-based radiosonde observations; and, radiosonde observations of geopotential height and wind using daily statistics from the gdas1 prepbufr files at 00Z; 06Z; 12Z; and, 18Z.
</li>
<br>
<li>
The SUMAC4 programs are set up to verify gridded forecast fields against quality- controlled radiosondes. Program sumac4 verifies the GFS model forecasts of height; temperature; relative humidity; and wind at 850MB; 500MB; 250MB; and 100MB against radiosonde observation.
</li>
<br>
<li>
The ANLVER verifies a 1-degree grid forecasts against a 1-degree grid analysis using the GFS model for the Northern Hemisphere; the Southern Hemisphere; and the Tropics. Daily S1 scores from the GFS are generated for the Mean Sea Level and the 500MB heights
</li>
<br>
