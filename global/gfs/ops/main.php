<img src="../../../style/images/gfs4c.png" alt="" width="150" />
<br><br>The National Centers for Environmental Prediction (NCEP)'s Global Forecast System (GFS) is the cornerstone of NCEP’s operational production suite of numerical guidance. NCEP’s global forecasts provide deterministic and probabilistic guidance out to 16 days. The GFS provides initial and/or boundary conditions for NCEP’s other models for regional, ocean, and wave prediction systems. The Environmental Modeling Center (EMC) is responsible for the enhancements, transitions-to-operations, and maintenance of the GFS. The global data assimilation and forecasts are made four times daily at 0000, 0600, 1200 and 1800 UTC by NCEP Central Operations (NCO). More documentation about the GFS can be found <a href="https://www.emc.ncep.noaa.gov/emc/pages/numerical_forecast_systems/gfs/documentation.php" target="_blank">here</a>.
<br><br>The current operational version of the GFS is version 16.2 as of 2022 June 26. The service change notice can be found <a href="https://www.weather.gov/media/notification/pdf2/scn21-52_gfsv16.1.1.pdf" target="_blank">here</a>. Verification for the operational GFS and other operational global models can be found by navigating the links to the left.
<br><br>Verification is done using <a href="https://github.com/NOAA-EMC/EMC_verif-global" target="_blank">EMC_verif-global</a>, which uses <a href="https://github.com/dtcenter/METplus" target="_blank">METplus</a>.
